package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
	"strings"
)

func main() {
	scanner := bufio.NewScanner(os.Stdin)
	rue := [7]int{}
	for scanner.Scan() {
		str := scanner.Text()
		tmp := strings.Split(str, "~")
		if len(tmp) != 2 {
			continue
		}
		ips := make([]int, 0)
		subnetMasks := make([]int, 0)
		for _, s := range strings.Split(tmp[0], ".") {
			n, err := strconv.Atoi(s)
			if err != nil {
				break
			}
			if n > 255 {
				break
			}
			ips = append(ips, n)
		}

		for _, s := range strings.Split(tmp[1], ".") {
			n, err := strconv.Atoi(s)
			if err != nil {
				break
			}
			if n > 255 {
				break
			}
			subnetMasks = append(subnetMasks, n)
		}
		if len(ips) != 4 || len(subnetMasks) != 4 {
			rue[5]++
			continue
		}
		var b, j uint32
		for i := 3; i >= 0; i-- {
			b = b + (uint32(subnetMasks[i]) << uint(j*8))
			j++
		}
		b = ^b + 1
		if (b&(b-1)) != 0 || subnetMasks[3] == 255 {
			rue[5]++
			continue
		}
		n := ips[0]
		if n >= 1 && n <= 126 {
			rue[0]++
		} else if n >= 128 && n <= 192 {
			rue[1]++
		} else if n >= 192 && n <= 223 {
			rue[2]++
		} else if n >= 224 && n <= 239 {
			rue[3]++
		} else if n >= 240 && n <= 255 {
			rue[4]++
		}
		if n == 10 {
			rue[6]++
		}
		if n == 172 && (ips[1] >= 16 && ips[1] <= 31) {
			rue[6]++
		}
		if n == 192 && ips[1] == 168 {
			rue[6]++
		}

	}
	fmt.Println(rue)

}

